Clock::Clock(DisplayApp* app,
        Controllers::DateTime& dateTimeController,
        Controllers::Battery& batteryController,
        Controllers::Ble& bleController) : Screen(app), currentDateTime{{}},
                                           dateTimeController{dateTimeController}, batteryController{batteryController}, bleController{bleController} {

  backgroundLabel = lv_label_create(lv_scr_act(), NULL);
  backgroundLabel->user_data = this;
  lv_obj_set_click(backgroundLabel, true);
  lv_obj_set_event_cb(backgroundLabel, event_handler);
  lv_label_set_long_mode(backgroundLabel, LV_LABEL_LONG_CROP);
  lv_obj_set_size(backgroundLabel, 240, 240);
  lv_obj_set_pos(backgroundLabel, 0, 0);
  lv_label_set_text(backgroundLabel, "");
   
  bitmap.header.always_zero = 0;
  bitmap.header.w = 24;
  bitmap.header.h = 24;
  bitmap.data_size = 24 * 24 * LV_COLOR_SIZE / 8;
  bitmap.header.cf = LV_IMG_CF_TRUE_COLOR;
  bitmap.data = bitmap_map;
  img_src = lv_img_create(lv_scr_act(), NULL);  
  lv_img_set_src(img_src, &bitmap);  
}

Clock::~Clock() {
  lv_obj_clean(lv_scr_act());
}

bool Clock::Refresh() {
  
  if((counter++ % 5) == 0){

    counter = 0;	
    
    pos_x += dx;
    pos_y += dy;	    
	  
    lv_obj_set_pos(img_src, pos_x, pos_y);
  
    //checks if it has touched the sides (floor and ceiling)	  
    if(pos_y <= 0 || pos_y >= 216){
      dy *= -1;
    }
  
    //checks if it has touched the side (left and right)  
    if(pos_x <= 0 || pos_x >= 216){
      dx *= -1; 
    }  
  }      
  
return true;
}

bool Clock::OnButtonPushed() {
  running = false;
  return false;
}


